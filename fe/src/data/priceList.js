const random = (number) => Math.floor(Math.random() * number + 10);

const makePriceData = () => {
    const result = [];
    for (let i = 1; i <= 100; i++) {
        const a_price = i * 10000;
        const b_price = (100 - i) * 10000;
        let max = 0;
        if (i < 20) max = random(23);
        else if (i < 30) max = random(24);
        else max = random(25);
        let j = 0;
        while (j < max) {
            result.push(a_price);
            result.push(b_price);
            j++;
        }
    }
    return result;
};

export const mockupPriceListData = makePriceData();
